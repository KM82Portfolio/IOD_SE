function sum(a,b){
    return a+b;
}
function subtract(a,b){
    return a-b;
}
function multiply(a,b){
    return a*b;
}
function divide(a,b){
    return a/b;
}

console.log("5+2 = ",sum(5,2));
console.log("5-2 = ",subtract(2,5));
console.log("5*2 = ",multiply(5,2));
console.log("5/2 = ",divide(5,2));